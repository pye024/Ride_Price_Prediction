CREATE TABLE CLEANING AS SELECT DISTINCT * from PROJECT.PUBLIC.RIDE;

DELETE from PROJECT.PUBLIC.CLEANING
WHERE TRIP_DURATION is NULL OR
DISTANCE_TRAVELED is NULL OR
NUM_OF_PASSENGERS is NULL OR
FARE is NULL OR
TOTAL_FARE is NULL;

UPDATE PROJECT.PUBLIC.CLEANING
SET SURGE_APPLIED = COALESCE (SURGE_APPLIED,0),
TIP = COALESCE (TIP,0),
MISCELLANEOUS_FEES= COALESCE (MISCELLANEOUS_FEES,0)
;

DELETE FROM PROJECT.PUBLIC.CLEANING
WHERE NUM_OF_PASSENGERS = 0
   OR TRIP_DURATION = 0
   OR DISTANCE_TRAVELED = 0
   OR WHERE TOTAL_FARE = 0;
